package com.example.Rozvrh.Group;

public class Group {
    public Long id;
    private String name;

    public Group() {
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
